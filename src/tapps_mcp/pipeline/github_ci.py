_COPILOT_SETUP_STEPS = """\
# .github/workflows/copilot-setup-steps.yml
# Generated by TappsMCP tapps_init
# This workflow configures the environment for the Copilot coding agent.
# See: https://docs.github.com/en/copilot/using-github-copilot/using-copilot-coding-agent
name: Copilot Setup Steps

on: workflow_dispatch

permissions:
  contents: read

jobs:
  copilot-setup:
    runs-on: ubuntu-latest
    timeout-minutes: 10
    steps:
      - name: Checkout
        uses: actions/checkout@v4

      - name: Set up Python
        uses: actions/setup-python@v5
        with:
          python-version: "3.12"

      - name: Install project dependencies
        run: |
          pip install uv
          uv sync --frozen 2>/dev/null || pip install -e ".[dev]" 2>/dev/null || pip install -e . 2>/dev/null || true

      - name: Install quality checkers
        run: |
          pip install tapps-mcp ruff mypy bandit radon vulture pip-audit

      - name: Verify tools
        run: |
          echo "--- TappsMCP ---"
          tapps-mcp doctor 2>/dev/null || echo "tapps-mcp doctor not available"
          echo "--- Ruff ---"
          ruff version
          echo "--- Mypy ---"
          mypy --version
          echo "--- Bandit ---"
          bandit --version
"""

