def generate_enhanced_ci_workflow(project_root: Path) -> dict[str, Any]:
    """Generate enhanced ``.github/workflows/tapps-quality.yml``.

    Replaces the basic workflow with one that includes Artifacts v4,
    permissions, concurrency, and ``workflow_dispatch`` support.

    Returns a summary dict with ``file`` and ``action``.
    """
    wf_dir = project_root / ".github" / "workflows"
    wf_dir.mkdir(parents=True, exist_ok=True)
    target = wf_dir / "tapps-quality.yml"
    existed = target.exists()
    target.write_text(_QUALITY_WORKFLOW, encoding="utf-8")
    return {
        "file": str(target.relative_to(project_root)),
        "action": "updated" if existed else "created",
    }


