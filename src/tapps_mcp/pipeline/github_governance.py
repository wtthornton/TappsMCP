def generate_security_policy(project_root: Path) -> dict[str, Any]:
    """Generate ``SECURITY.md`` in the project root.

    Returns a summary dict with ``file`` and ``action``.
    """
    target = project_root / "SECURITY.md"
    existed = target.exists()
    if existed:
        return {"file": "SECURITY.md", "action": "skipped", "reason": "already_exists"}
    target.write_text(_SECURITY_POLICY, encoding="utf-8")
    return {"file": "SECURITY.md", "action": "created"}


def generate_codeowners(project_root: Path) -> dict[str, Any]:
    """Generate ``.github/CODEOWNERS`` template.

    Returns a summary dict with ``file`` and ``action``.
    """
    github_dir = project_root / ".github"
    github_dir.mkdir(parents=True, exist_ok=True)
    target = github_dir / "CODEOWNERS"
    if target.exists():
        return {
            "file": str(target.relative_to(project_root)),
            "action": "skipped",
            "reason": "already_exists",
        }
    target.write_text(_CODEOWNERS_TEMPLATE, encoding="utf-8")
    return {"file": str(target.relative_to(project_root)), "action": "created"}


def generate_ruleset_scripts(project_root: Path) -> dict[str, Any]:
    """Generate ruleset setup scripts in ``.github/scripts/``.

    Creates both bash (``.sh``) and PowerShell (``.ps1``) scripts.

    Returns a summary dict with ``files`` and ``action``.
    """
    import stat

    scripts_dir = project_root / ".github" / "scripts"
    scripts_dir.mkdir(parents=True, exist_ok=True)

    files_written: list[str] = []

    sh_target = scripts_dir / "setup-rulesets.sh"
    sh_target.write_text(_RULESET_SETUP_SH, encoding="utf-8")
    sh_target.chmod(sh_target.stat().st_mode | stat.S_IXUSR | stat.S_IXGRP)
    files_written.append(str(sh_target.relative_to(project_root)))

    ps1_target = scripts_dir / "setup-rulesets.ps1"
    ps1_target.write_text(_RULESET_SETUP_PS1, encoding="utf-8")
    files_written.append(str(ps1_target.relative_to(project_root)))

    return {"files": files_written, "action": "created", "count": len(files_written)}


def generate_setup_guide(project_root: Path) -> dict[str, Any]:
    """Generate ``docs/GITHUB_SETUP_GUIDE.md``.

    Returns a summary dict with ``file`` and ``action``.
    """
    docs_dir = project_root / "docs"
    docs_dir.mkdir(parents=True, exist_ok=True)
    target = docs_dir / "GITHUB_SETUP_GUIDE.md"
    target.write_text(_SETUP_GUIDE, encoding="utf-8")
    return {"file": str(target.relative_to(project_root)), "action": "created"}


def generate_all_governance(project_root: Path) -> dict[str, Any]:
    """Generate all governance and security configuration files.

    Convenience function that calls all individual generators.

    Returns a summary dict with sub-results.
    """
    results: dict[str, Any] = {}
    results["security_policy"] = generate_security_policy(project_root)
    results["codeowners"] = generate_codeowners(project_root)
    results["ruleset_scripts"] = generate_ruleset_scripts(project_root)
    results["setup_guide"] = generate_setup_guide(project_root)

    total_files = (
        1  # SECURITY.md
        + 1  # CODEOWNERS
        + results["ruleset_scripts"]["count"]
        + 1  # setup guide
    )
    results["total_files"] = total_files
    results["success"] = True
    return results
